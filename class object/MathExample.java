class MathExample 
{
	public static void main(String[] args) 
	{
       System.out.println(Math.abs(43.56789234f));
       System.out.println("Math.min(4,9)="+Math.min(4,9));
       System.out.println("Math.max(4,9)="+Math.max(4,9));
       System.out.println("Math.round(43.56789234f)="+Math.round(43.56789234f));

		System.out.println("Math.pow(2,3)="+Math.pow(2.2,3));


		System.out.println("Math.random()="+(int)(Math.random()*10));
	}
}
