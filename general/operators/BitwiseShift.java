class BitwiseShift
     {
      public static void main(String args[])
         {
	 int a=9;
	 int b=2;
	 int c;
	 System.out.println("a="+a);
	System.out.println("b="+b);
	c=a<<b;//Bit-wise left shift operation
	System.out.println("a<<b="+c);
	c=a>>b;//Bit-wise left shift operation
	System.out.println("a>>b="+c);
	}
  }